
package model;

public class Contador {
   
    private String frase;
    private int vogais;
    
    public Contador() {
        this.vogais = vogais;
        this.frase = frase;
    }
    
    public String getFrase(){
        return frase;
    }
    
    public void setFrase(String frase){
        this.frase = frase;
    }
    
     public int getVogais(){
        return vogais;
    }
    
    public void setVogais(int vogais){
        this.vogais = vogais;
    }
}
